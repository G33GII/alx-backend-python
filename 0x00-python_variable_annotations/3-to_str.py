#!/usr/bin/env python3
"""
This module provides a function to calculate the floor of a float.
"""

import math


def floor(n: float) -> int:
    """
    Calculate the floor of a given float.

    Args:
        n (float): The number to calculate the floor of.

    Returns:
        int: The floor of the input number.
    """
    return math.floor(n)
